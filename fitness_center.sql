-- Create the database
DROP DATABASE IF EXISTS ConanFitness_db;

CREATE DATABASE IF NOT EXISTS ConanFitness_db;
USE ConanFitness_db;

-- 1. CORE TABLES
CREATE TABLE Center (
    center_id INT AUTO_INCREMENT PRIMARY KEY,
    center_name VARCHAR(100) UNIQUE NOT NULL,
    address VARCHAR(255) NOT NULL
);

CREATE TABLE Room (
    room_id INT AUTO_INCREMENT PRIMARY KEY,
    center_id INT NOT NULL,
    room_number INT NOT NULL,
    max_capacity INT NOT NULL,
    FOREIGN KEY (center_id) REFERENCES Center(center_id) ON DELETE CASCADE,
    UNIQUE (center_id, room_number)
);

CREATE TABLE Person (
    person_id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(50) NOT NULL,
    family_name VARCHAR(50) NOT NULL,
    birth_date DATE NOT NULL,
    UNIQUE (first_name, family_name, birth_date)
);

-- 2. MEMBER AND TRAINER TABLES (Updated with experience)
CREATE TABLE Member (
    member_id INT AUTO_INCREMENT PRIMARY KEY,
    person_id INT NOT NULL,
    join_date DATE NOT NULL DEFAULT (NOW()),
    FOREIGN KEY (person_id) REFERENCES Person(person_id) ON DELETE CASCADE,
    UNIQUE (person_id)
);

CREATE TABLE Trainer (
    trainer_id INT AUTO_INCREMENT PRIMARY KEY,
    person_id INT NOT NULL,
    diploma VARCHAR(100) NOT NULL,
    hire_date DATE NOT NULL DEFAULT (NOW()),
    experience VARCHAR(50) COMMENT 'Years of experience or level',
    FOREIGN KEY (person_id) REFERENCES Person(person_id) ON DELETE CASCADE,
    UNIQUE (person_id)
);

-- 3. SESSION TABLES
CREATE TABLE Session (
    session_id INT AUTO_INCREMENT PRIMARY KEY,
    session_date DATE NOT NULL,
    start_hour TIME NOT NULL,
    room_id INT NOT NULL,
    FOREIGN KEY (room_id) REFERENCES Room(room_id) ON DELETE CASCADE,
    UNIQUE (session_date, start_hour, room_id)
);

CREATE TABLE IndividualSession (
    session_id INT PRIMARY KEY,
    member_id INT NOT NULL,
    FOREIGN KEY (session_id) REFERENCES Session(session_id) ON DELETE CASCADE,
    FOREIGN KEY (member_id) REFERENCES Member(member_id) ON DELETE CASCADE
);

CREATE TABLE GroupSession (
    session_id INT PRIMARY KEY,
    session_type VARCHAR(50) NOT NULL,
    trainer_id INT NOT NULL,
    FOREIGN KEY (session_id) REFERENCES Session(session_id) ON DELETE CASCADE,
    FOREIGN KEY (trainer_id) REFERENCES Trainer(trainer_id) ON DELETE CASCADE
);

CREATE TABLE SessionParticipation (
    participation_id INT AUTO_INCREMENT PRIMARY KEY,
    session_id INT NOT NULL,
    member_id INT NOT NULL,
    FOREIGN KEY (session_id) REFERENCES GroupSession(session_id) ON DELETE CASCADE,
    FOREIGN KEY (member_id) REFERENCES Member(member_id) ON DELETE CASCADE,
    UNIQUE (session_id, member_id)
);

-- 4. NEW ENTITIES (Activity, Payment, Feedback, Equipment)
CREATE TABLE Activity (
    activity_id INT AUTO_INCREMENT PRIMARY KEY,
    activity_name VARCHAR(100) NOT NULL,
    intensity VARCHAR(50) NOT NULL,
    duration INT NOT NULL COMMENT 'Duration in minutes',
    max_capacity INT,
    session_id INT NOT NULL,
    FOREIGN KEY (session_id) REFERENCES Session(session_id) ON DELETE CASCADE,
    UNIQUE (session_id)
);

CREATE TABLE Payment (
    payment_id INT AUTO_INCREMENT PRIMARY KEY,
    member_id INT NOT NULL,
    amount DECIMAL(10,2) NOT NULL,
    payment_date DATE NOT NULL,
    payment_method VARCHAR(50),
    payment_status VARCHAR(20) DEFAULT 'Pending',
    FOREIGN KEY (member_id) REFERENCES Member(member_id) ON DELETE CASCADE
);

CREATE TABLE Feedback (
    feedback_id INT AUTO_INCREMENT PRIMARY KEY,
    member_id INT NOT NULL,
    session_id INT NOT NULL,
    rating INT NOT NULL CHECK (rating BETWEEN 1 AND 5),
    comments TEXT,
    feedback_date DATE NOT NULL,
    trainer_id INT,
    FOREIGN KEY (member_id) REFERENCES Member(member_id) ON DELETE CASCADE,
    FOREIGN KEY (session_id) REFERENCES Session(session_id) ON DELETE CASCADE,
    FOREIGN KEY (trainer_id) REFERENCES Trainer(trainer_id) ON DELETE SET NULL
);

-- NEW EQUIPMENT TABLE
CREATE TABLE Equipment (
    equipment_id INT AUTO_INCREMENT PRIMARY KEY,
    room_id INT NOT NULL,
    type VARCHAR(100) NOT NULL,
    condition_status VARCHAR(50) DEFAULT 'Good',
    purchase_date DATE,
    last_maintenance_date DATE,
    FOREIGN KEY (room_id) REFERENCES Room(room_id) ON DELETE CASCADE
);

-- 5. PERFORMANCE INDEXES
CREATE INDEX idx_activity_session ON Activity(session_id);
CREATE INDEX idx_payment_member ON Payment(member_id);
CREATE INDEX idx_payment_date ON Payment(payment_date);
CREATE INDEX idx_feedback_member ON Feedback(member_id);
CREATE INDEX idx_feedback_session ON Feedback(session_id);
CREATE INDEX idx_feedback_trainer ON Feedback(trainer_id);
CREATE INDEX idx_session_room ON Session(room_id);
CREATE INDEX idx_group_trainer ON GroupSession(trainer_id);
CREATE INDEX idx_member_person ON Member(person_id);
CREATE INDEX idx_trainer_person ON Trainer(person_id);
CREATE INDEX idx_equipment_room ON Equipment(room_id);
